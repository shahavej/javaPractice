package OperatorAndLoops;

public class Demo3 {

		//* this is the example of switch statement 
		
	public static void main(String[] args) {
		
		switch (5) {
		case 1:
		{
			System.out.println("you have entered 1");
			break;
		}
		case 2:
		{
			System.out.println("you have entered 2");
			break;
		}

		default:
			System.out.println("you have entered wrong key");
			break;
		}
		
	}
}
