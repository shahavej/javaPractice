package OperatorAndLoops;

public class Demo4 {
	
	//* this is the example of nested if else
	
	public static void main(String[] args) {

		int n=9;
		if (n>10) {

			System.out.println(n  +": is grater than 10");
		}
		else if (n==10) {
			System.out.println(n + ": is equals to 10");
		}
		else {
			System.out.println(n + ": is less than 10");
		}
	} 
}
